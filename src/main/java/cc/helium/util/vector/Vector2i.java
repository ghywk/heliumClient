package cc.helium.util.vector;

public final class Vector2i {
    public int x, y;

    public Vector2i() {
    }

    public Vector2i(final int x, final int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
